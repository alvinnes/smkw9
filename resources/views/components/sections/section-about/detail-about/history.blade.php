    <h2 class="font-semibold text-3xl">Sejarah SMK Walisongo Pecangaan</h2>
    <p>SMK Walisongo Pecangaan merupakan lembaga pendidikan termuda di bawah naungan Yayasan Walisongo
        Pecangaan, Jepara. Keberadaan SMK Walisongo adalah upaya pengembangan di bidang pendidikan dari
        Yayasan
        Walisongo melalui lembaga Madrasah Aliyah. Yayasan Walisongo sendiri memiliki sejarah panjang yang
        dimulai dari berdirinya lembaga pendidikan Nahdlatul Ulama pada tanggal 5 Agustus 1965 bernama
        Muallimin
        NU, yang menjadi cikal bakal bagi seluruh lembaga pendidikan yang dikelola oleh yayasan ini.</p>

    <p>Pada awalnya, lembaga ini bernama SMK Kelas Jauh di MA Walisongo. SMK Kelas Jauh di MA Walisongo
        mulai
        menyelenggarakan kegiatan pendidikan dan pelatihan pada tahun pelajaran 2004/2005 dengan Program
        Keahlian Kriya Tekstil, berdasarkan Surat Kesepakatan pembukaan SMK Kelas Jauh antara Madrasah
        Aliyah
        Walisongo dengan SMK Negeri 2 Jepara yang ditandatangani pada tanggal 12 Juni 2004. Surat
        kesepakatan
        tersebut dikuatkan dengan Surat Keputusan Kepala Dinas Pendidikan dan Kebudayaan Kabupaten Jepara
        No.
        421.5/00171 tentang Penunjukan SMK Negeri 2 Jepara menjadi SMK Induk bagi SMK Kelas Jauh di MA
        Walisongo
        Pecangaan.</p>

    <p>Satu tahun kemudian, SMK kelas jauh di MA Walisongo mulai berkembang dengan membuka program keahlian
        baru
        yaitu Teknik Mekanik Otomotif (sekarang menjadi Teknik Kendaraan Ringan Otomotif). Seiring
        perkembangannya, pada tahun ke-3 yaitu tahun pelajaran 2007/2008, SMK Kelas Jauh di MA Walisongo
        mengajukan izin operasional mandiri ke Bupati Jepara. Alhamdulillah, dengan rahmat Allah dan
        perjuangan
        semua stakeholder sekolah, pada tanggal 26 Juli 2007 SMK Kelas Jauh di MA Walisongo mendapat izin
        operasional mandiri dengan nomor surat 421.4/3290. Dengan demikian, SMK Kelas Jauh di MA Walisongo
        resmi
        menjadi lembaga/unit mandiri dengan nama SMK Walisongo Pecangaan.</p>

    <p>Sekolah ini didirikan dengan Nomor SK Pendirian 425.1/00546 pada tanggal 15 Maret 2005. Keberadaan
        seluruh lembaga pendidikan di bawah Yayasan Walisongo semakin kuat secara yuridis setelah dikelola
        oleh
        yayasan yang berbadan hukum melalui Akte Notaris J. Moeljani, SH Nomor 100 pada tanggal 15 Februari
        1980.</p>
